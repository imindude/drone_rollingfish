#include "drv_led.h"
#include "hal_intf.h"

/* ****************************************************************************************************************** */

struct sLedData
{
    hHalGpio    _led;
};
typedef struct sLedData     sLedData;

/* ****************************************************************************************************************** */

static sLedData     led_data;

/* ****************************************************************************************************************** */

bool drv_led_init(void)
{
    led_data._led = hal_gpio_init(eHalGpio_LED);
    return true;
}

void drv_led_on(void)
{
    hal_gpio_reset(led_data._led);
}

void drv_led_off(void)
{
    hal_gpio_set(led_data._led);
}

